  private boolean ehAlfanumerico(String s) {
    Character c = null;
    for(int i = 0; i < s.length(); i++) {
      c = s.charAt(i);
      if(!Character.isLetterOrDigit(c) && !Character.isSpaceChar(c)) {
        return false;
      }
    }

    return true;
  }

    private void b_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_cadastrarActionPerformed

      controlador.salvaRegistro(6002, controlador.getUsuarioCorrente().getLogin());

      String nome = tf_nome.getText();
      String login = tf_login.getText();

      if(nome.length() > 50 || nome.length() == 0) {
        JOptionPane.showMessageDialog(this.getContentPane(),
                                      "Campo do nome: nao pode ser vazio e nao pode ter mais do que 50 caracteres.");
        return;
      }

      if(!ehAlfanumerico(nome)) {
        JOptionPane.showMessageDialog(this.getContentPane(),
                                      "Campo do nome: contem caracteres que nao sao alfanumericos.");
        return;
      }

      if(login.length() > 20 || login.length() == 0) {
        JOptionPane.showMessageDialog(this.getContentPane(),
                                      "Campo de login: nao pode ser vazio e nao pode ter mais do que 20 caracteres.");
        return;
      }

      if(controlador.existeUsuario(tf_login.getText())) {
        JOptionPane.showMessageDialog(this.getContentPane(),
                                      "Campo de login: usuario ja existe.");
        return;
      }

      if(!ehAlfanumerico(login)) {
        JOptionPane.showMessageDialog(this.getContentPane(),
                                      "Campo do login: contem caracteres que nao sao alfanumericos.");
        return;
      }

      char[] senha = pf_CadastraSenhaPessoal.getPassword();
      char[] senhaConfirma = pf_CadastraSenhaPessoalConfirma.getPassword();

      if((senha.length > 6 || senha.length == 0)) {
        JOptionPane.showMessageDialog(this.getContentPane(),
                                      "Campo de senha: nao pode ser vazio e nao pode ter mais do que 3 fonemas.");
        return;
      }

      if(senhaConfirma.length > 6 || senhaConfirma.length == 0) {
        JOptionPane.showMessageDialog(this.getContentPane(),
                                      "Campo de Confirmacao de senha: nao pode ser vazio e nao pode ter mais do que 3 fonemas.");
        return;
      }

      if(String.valueOf(senha).compareTo(String.valueOf(senhaConfirma)) != 0) {
        JOptionPane.showMessageDialog(this.getContentPane(),
                                      "Senhas diferentes.");
        return;
      }

      controlador.cadastraUsuario(nome,
                                  login,
                                  (String) cb_grupo.getSelectedItem(),
                                  String.valueOf(senha),
                                  Integer.parseInt(cb_quantidadeSenhasCartao.getSelectedItem().toString()));

      JOptionPane.showMessageDialog(this.getContentPane(), login + ": cadastrado com sucesso.");

      mostraTelaPrincipal();

      tf_nome.setText("");
      tf_login.setText("");
      pf_CadastraSenhaPessoal.setText("");
      pf_CadastraSenhaPessoalConfirma.setText("");
      cb_grupo.setSelectedIndex(0);
      cb_quantidadeSenhasCartao.setSelectedIndex(0);
    }//GEN-LAST:event_b_cadastrarActionPerformed


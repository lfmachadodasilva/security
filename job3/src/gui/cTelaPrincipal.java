  public void mostraTelaPrincipal() {
    p_telaPrincipal.removeAll();

    p_telaPrincipal.add(p_cabecalho);
    p_telaPrincipal.add(p_totalSUV);
    p_telaPrincipal.add(p_menuPrincipal);

    p_telaPrincipal.validate();
    p_telaPrincipal.repaint();
  }


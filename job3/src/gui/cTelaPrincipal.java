    private void b_SairActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_SairActionPerformed

      controlador.salvaRegistro(9002, controlador.getUsuarioCorrente().getLogin());

      if(controlador.retornaSenhasLivres().isEmpty()) {
       JOptionPane.showMessageDialog(this.getContentPane(),
                                      "Para sair, eh necessario ter pelo menos uma senha de uso unico.\n"
                                      + "Pressione o botao Voltar de Sair para o Menu Principal.");
      } else {

        controlador.deslogaUsuario();
      }

    }//GEN-LAST:event_b_SairActionPerformed

